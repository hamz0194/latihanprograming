$x = 20;
if ($x < 20) {
  echo "benar"; // jika nilai x lebih kecil dari 20 akan tampil benar
} elseif ($x == 20) { // jika nilai x sama dengan 20 akan tampil bingo!
  echo "bingo!";
} else {
  echo "salah"; // jika nilai x tidak sama dengan/lebih besar dari 20 maka tampil salah
}
